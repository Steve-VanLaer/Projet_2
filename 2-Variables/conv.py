# Convertir une variable string en type int
age = int(input('Introduisez votre âge : '))
print(type(age))

# Convertir une variable float en type int
a = 2.3
b = int(a)
print(type(b))     

# Un calcul qui fait intervenir des variables de type int et float donne automatiquement un résulat de type float
a = 5.4
b = 2
print(type(a*b))

# Convertir un nombre en texte
texte = str(a*b)
print(type(texte))