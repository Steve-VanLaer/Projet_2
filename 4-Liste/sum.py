# Afficher la plus grande valeur d'une liste
liste = [1.1, 1.2, 1.3, 1.4, 1.5]
print(liste)
print('Le max est',max(liste))

# Afficher la plus petite valeur d'une liste
liste = list(range(0,10))
print(liste)
print('Le min est',min(liste))

# Affichier la somme d'une liste
print('La somme est ',sum(liste))