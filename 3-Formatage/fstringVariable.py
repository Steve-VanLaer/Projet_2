# Introduire un entier dans un f-string
nbre = int(input('Introduisez un entier : '))
print(f"Le nombre introduit est {nbre}.")

# Introduisez un nombre décimal dans un f-string
nbre = float(input('Introduisez un nombre décimale : '))
print(f'Le nombre déciamal introduit est {nbre}')           # Python affiche par défaut une décimale

# Introduire une chaîne de caractères dans un f-string
texte = input('Introduisez votre prénom : ')
print(f"Bonjour, {texte}.")